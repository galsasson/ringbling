<h3 id="stl">What is an .stl file?</h2>
  <p>A .stl file is the 3D model file that a 3D printer can read to print your Ring Bling ring splint.</p>
<h3 id="stlnextsteps">I have the .stl file. What now?</h3>
  <p>Find a 3D printing service locally or on the web, and transfer or upload your .stl file following their instructions. You can further customize your ring splints by choosing different materials and colors through your chosen 3D printing service.</p>
<h3 id="materials">What materials can I use to print my Ring Bling ring splints?</h3>
  <p>We recommend the use of smooth, rigid metals and plastics, as flexible materials will undermine the purpose of the ring splints. We are currently researching various alternatives and we’ll update this page as soon as we have identified the best options.</p>
<h3 id="color">How do I change the color of my Ring Bling?</h3>
  <p>You can choose different colors and materials through your chosen 3D printing service.</p>
<h3 id="print">Can you print my Ring Bling for me?</h3>
  <p>The short answer: not yet, but we hope to change that soon. We are working on a way to integrate 3D printing into our site so that you will be able to customize, style, and order your ring splints all in one place—and re-order them as needed.</p>
